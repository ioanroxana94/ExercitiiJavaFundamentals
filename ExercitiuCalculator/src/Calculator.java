public class Calculator {

    public Calculator(){

    }

    public int adunare(int a, int b){
        int adunare= a+b;
        return adunare;  // sau putem pune simplu " return a+b; "
    }

    public int scadere(int a, int b){
        return a - b;
    }

    public int inmultire(int a, int b){
        return a * b;
    }

    public int impartire(int a, int b){
        return a / b;
    }
}
